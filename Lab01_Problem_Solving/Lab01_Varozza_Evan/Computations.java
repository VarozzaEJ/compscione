public class Computations {
    public static void main(String[] args) {
        //Variable Declarations and Assignments
        double variableOne = 4.5;
        double variableTwo = 4.5;

        System.out.println("The sum of " + variableOne + " and " + variableTwo + " is: " + (variableOne + variableTwo));
        System.out.println("The quotient of " + variableOne +  " and " + variableTwo + " is: " + (variableOne / variableTwo));
        System.out.println("The product of " + variableOne + " and " + variableTwo +  " is: " + (variableOne * variableTwo));
    }
}
